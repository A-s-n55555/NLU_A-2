def remove_repeatlines(text): # this will remove the repeated lines from text
    lines = text.split("\n")
    line_count = Counter(lines)

    cleaned_lines = [
        line for line in lines
        if line_count[line] < 3   # threshold can be changed ir required
    ]

    return "\n".join(cleaned_lines)

def remove_pn(text): # removes the page numbers using regular expression
    text = re.sub(r'\bPage\s*\d+\b', '', text)
    text = re.sub(r'^\d+\s*$', '', text, flags=re.MULTILINE)
    return text

def remove_metadata(text): # removes the other unnecessary data(url,isbn,etc)
    text = re.sub(r'doi:\s*\S+', '', text, flags=re.I)
    text = re.sub(r'ISBN\s*\S+', '', text, flags=re.I)
    text = re.sub(r'http\S+', '', text)
    text = re.sub(r'©\s*\d{4}', '', text)
    return text

def cleaned_text(text): # call all the cleaning functions
    text = remove_repeatlines(text)
    text = remove_pn(text)
    text = remove_metadata(text)
    return text
