def get_word_vectors(embeddings, words):
    valid_words = [w for w in words if w in word_to_idx]
    vectors = np.array([embeddings[word_to_idx[w]] for w in valid_words])
    return valid_words, vectors

def plot_pca(embeddings, words, title):

    labels, vectors = get_word_vectors(embeddings, words)

    pca = PCA(n_components=2)
    reduced = pca.fit_transform(vectors)

    plt.figure(figsize=(6,6))
    plt.scatter(reduced[:,0], reduced[:,1])

    for i, word in enumerate(labels):
        plt.annotate(word, (reduced[i,0], reduced[i,1]))

    plt.title(title)
    plt.xlabel("PC1")
    plt.ylabel("PC2")
    plt.show()

def plot_tsne(embeddings, words, title):

    labels, vectors = get_word_vectors(embeddings, words)

    tsne = TSNE(n_components=2, random_state=42, perplexity=5)
    reduced = tsne.fit_transform(vectors)

    plt.figure(figsize=(6,6))
    plt.scatter(reduced[:,0], reduced[:,1])

    for i, word in enumerate(labels):
        plt.annotate(word, (reduced[i,0], reduced[i,1]))

    plt.title(title)
    plt.xlabel("Dim 1")
    plt.ylabel("Dim 2")
    plt.show()

