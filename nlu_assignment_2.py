"""**Training Skip-gram with neg sampling**"""

window_size = 2

training_pairs = []

for sentence in net_corpus:
    sentence_idx = [word_to_idx[w] for w in sentence]

    for i, center in enumerate(sentence_idx):

        start = max(0, i-window_size)
        end = min(len(sentence_idx), i+window_size+1)

        for j in range(start, end):
            if i != j:
                context = sentence_idx[j]
                training_pairs.append((center, context))

embedding_dim = 120

W1_sg = np.random.randn(V, embedding_dim) * 0.01
W2_sg = np.random.randn(embedding_dim, V) * 0.01

"""**Negative sampling applied**"""

import numpy as np

def sigmoid(x):
    return 1 / (1 + np.exp(-x))

lr = 0.049
epochs = 20
K = 5   # number of negative samples

for epoch in range(epochs):

    loss = 0

    for center, context in training_pairs:

        # center word embedding
        h = W1_sg[center]

        # -------- Positive sample --------
        score_pos = np.dot(h, W2_sg[:, context])
        pred_pos = sigmoid(score_pos)

        loss -= np.log(pred_pos + 1e-10)

        grad_pos = pred_pos - 1

        dh = grad_pos * W2_sg[:, context]
        W2_sg[:, context] -= lr * grad_pos * h

        # -------- Negative samples --------
        neg_samples = np.random.choice(V, K)

        for neg in neg_samples:

            score_neg = np.dot(h, W2_sg[:, neg])
            pred_neg = sigmoid(score_neg)

            loss -= np.log(1 - pred_neg + 1e-10)

            grad_neg = pred_neg

            dh += grad_neg * W2_sg[:, neg]
            W2_sg[:, neg] -= lr * grad_neg * h

        # update center embedding
        W1_sg[center] -= lr * dh

    print("Epoch:", epoch, "Loss:", loss)
    print("Epoch:", epoch, "Avg Loss:", loss/len(training_pairs))

