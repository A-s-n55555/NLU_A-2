import numpy as np
from collections import Counter

# Flatten tokens
tokens = [word for doc in net_corpus for word in doc]

# Build vocabulary
vocab = list(set(tokens))
word_to_idx = {w:i for i,w in enumerate(vocab)}
idx_to_word = {i:w for w,i in word_to_idx.items()}

V = len(vocab)   # vocab size

"""**Trainning CBOW model**"""

window_size = 2

training_data = []

for sentence in net_corpus:
    sentence_idx = [word_to_idx[w] for w in sentence]

    for i in range(window_size, len(sentence_idx) - window_size):

        context = []
        for j in range(-window_size, window_size + 1):
            if j != 0:
                context.append(sentence_idx[i+j])

        target = sentence_idx[i]

        training_data.append((context, target))

N = 120   # embedding dimension

W1_cbow = np.random.randn(V, N) * 0.01
W2_cbow = np.random.randn(N, V) * 0.01

def softmax(x):
    e = np.exp(x - np.max(x))
    return e / np.sum(e)

lr = 0.049
epochs = 20

for epoch in range(epochs):

    loss = 0

    for context, target in training_data:

        # ----- Forward pass -----

        # average context embeddings
        h = np.mean(W1_cbow[context], axis=0)   # hidden layer

        u = np.dot(h, W2_cbow)                  # output
        y_pred = softmax(u)

        eps = 1e-10
        loss -= np.log(y_pred[target] + eps)

        # ----- Backprop -----

        y_true = np.zeros(V)
        y_true[target] = 1

        e = y_pred - y_true

        dW2 = np.outer(h, e)
        dh = np.dot(W2_cbow, e)

        # distribute gradient to context words
        for word in context:
            W1_cbow[word] -= lr * dh / len(context)

        W2_cbow -= lr * dW2

    print("Epoch:", epoch, "Loss:", loss)
    print("Epoch:", epoch, "Avg Loss:", loss/len(training_data))

