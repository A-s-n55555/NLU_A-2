import numpy as np

def train_model(model, data, seq_len=10, epochs=50):
    losses = []

    # Determine hidden dimension dynamically
    H_dim = model.H if hasattr(model, 'H') else model.hidden_dim

    for epoch in range(epochs):
        total_loss = 0

        # Initialize Forward states ONCE per epoch (Stateful across contiguous chunks)
        h_prev = np.zeros((H_dim, 1))
        h_prev_f = np.zeros((H_dim, 1))
        c_prev_f = np.zeros((H_dim, 1))

        # Step by seq_len to ensure chunks are strictly contiguous!
        # We need seq_len + 1 characters total to get a sequence of inputs and a sequence of targets
        for i in range(0, len(data) - seq_len, seq_len):

            # Slice the contiguous chunk
            seq = data[i : i + seq_len + 1]
            inputs_seq = seq[:-1]
            targets_seq = seq[1:]

            # --- Vanilla RNN ---
            if type(model).__name__ == "Vanilla_RNN":
                current_loss, h_prev = model.train_step(inputs_seq, h_prev, targets_seq)
                total_loss += current_loss

            # --- Bidirectional LSTM ---
            elif type(model).__name__ == "BiLSTM":
                # MUST reset backward states for every chunk (we don't know the "future" state)
                h_b_next = np.zeros((H_dim, 1))
                c_b_next = np.zeros((H_dim, 1))

                # Note: Signature matches the corrected BLSTM we wrote earlier
                current_loss, h_prev_f, c_prev_f, _, _ = model.train_step(inputs_seq, targets_seq, h_prev_f, c_prev_f, h_b_next, c_b_next)
                total_loss += current_loss

            # --- RNN with Attention ---
            elif type(model).__name__ == "RNN_Attention":
                # Reset states per sequence. Seq2Seq models don't typically carry state across chunks
                h_enc_prev_attn = np.zeros((H_dim, 1))
                h_dec_prev_attn = np.zeros((H_dim, 1))

                current_loss, _, _ = model.train_step(inputs_seq, h_enc_prev_attn, h_dec_prev_attn, targets_seq)
                total_loss += current_loss

        losses.append(total_loss)
        print(f"Epoch: {epoch} | Loss: {total_loss:.4f}")

    return losses



