import numpy as np

def generate_name(model, start_char, c_to_idx, idx_to_char, vocab_size, length=12):
    """
    Generates a name character-by-character using the specified model.
    """
    start_idx = c_to_idx[start_char]
    name = start_char
    current_char_idx = start_idx

    # ==========================================
    # 1. Vanilla RNN Generation (Working!)
    # ==========================================
    if type(model).__name__ == 'Vanilla_RNN':
        h_prev = np.zeros((model.hidden_dim, 1))
        for _ in range(length - 1):
            probs, h_prev = model.forward(current_char_idx, h_prev)
            current_char_idx = np.random.choice(vocab_size, p=probs.ravel())
            name += idx_to_char[current_char_idx]

    # ==========================================
    # 2. BiLSTM Generation (The Dummy Sequence Hack)
    # ==========================================
    elif type(model).__name__ == 'BiLSTM':
        # 1. Create a dummy sequence of the target length.
        # We start with the start_idx, and fill the rest with random characters
        # so the backward pass has something to read.
        dummy_sequence = [start_idx] + [np.random.choice(vocab_size) for _ in range(length - 1)]

        # 2. Call the method EXACTLY as the traceback expects: (input_sequence, idx_to_char)
        output = model.predict_sequence(dummy_sequence, idx_to_char)

        # 3. Format the output
        if isinstance(output, list):
            name = "".join(output)
        elif isinstance(output, str):
            name = output
        else:
            name = str(output)

   # ==========================================
    # 3. RNN Attention Generation (FIXED)
    # ==========================================
    elif type(model).__name__ == 'RNN_Attention':
        initial_encoder_input = [start_idx]
        h_enc_init = np.zeros((model.hidden_dim, 1))
        h_dec_init = np.zeros((model.hidden_dim, 1))

        # Passing ALL arguments in the exact order the traceback expects
        generated_string = model.forward(
            initial_encoder_input,
            start_idx,
            h_enc_init,
            h_dec_init,
            idx_to_char,  # <-- Added the missing dictionary here!
            length
        )

        # Prevent duplicating the start character if the model already prepends it
        if isinstance(generated_string, str):
             name = generated_string
        elif isinstance(generated_string, list):
             name = "".join(generated_string)
        else:
             name = str(generated_string)

    else:
        raise ValueError(f"Unknown model type: {type(model).__name__}")

    # Ensure the final string is nicely formatted (removing newlines for cleaner printing)
    return name[:length].replace('\n', ' ')

