def compute_metrics(generated_names, training_data):
    """
    Computes Novelty Rate and Diversity for a list of generated names.
    """
    if not generated_names:
        return 0.0, 0.0

    # Convert training data to a set for O(1) lookups
    training_set = set(training_data)
    total_generated = len(generated_names)

    # 1. Novelty Rate: (Generated names not in training set) / (Total generated names)
    novel_names = [name for name in generated_names if name not in training_set]
    novelty_rate = (len(novel_names) / total_generated) * 100

    # 2. Diversity: (Unique generated names) / (Total generated names)
    unique_generated = set(generated_names)
    diversity = len(unique_generated) / total_generated

    return novelty_rate, diversity

